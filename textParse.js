readable.on('end', function(){
	// to check valid Number
	for(var peopleNum = 0; peopleNum < people.length; peopleNum++){
	    for(var groupNum = 0; groupNum < people[peopleNum].length; groupNum++){  	
	    	checkNumDupInGroup(people[peopleNum][groupNum],peopleNum,groupNum);
	    	checkInvalidNum(people[peopleNum][groupNum],peopleNum,groupNum);
	    }
	}
	function checkNumDupInGroup(arrGroup, peopleNum, groupNum) {
		for(var i =0 ; i < arrGroup.length; i++){
			for(var j = i+1; j < arrGroup.length; j++){
				arrGroup.sort(function(a, b){return a-b});
				if( arrGroup[i] == arrGroup[j] ){		
					console.log("숫자 중복 : people " + (peopleNum+1) + " group "+ (groupNum+1) + " num "+ arrGroup[i] );
				}
			}
		}
	}
	function checkInvalidNum(arrGroup, peopleNum, groupNum) {
		for(var i =0 ; i < arrGroup.length; i++){
			for(var j = i+1; j < arrGroup.length; j++){
				arrGroup.sort(function(a, b){return a-b});
				if( (arrGroup[i] < 1) || (72 < arrGroup[i]) || isNaN(arrGroup[i])){
					console.log("예외숫자 등장 : people " + (peopleNum+1) + " group "+ (groupNum+1) + " num "+ arrGroup[j] ); 
				}
			}
		}
	}
	
	/*
	 * resultTable에 입력하는 코드
	 */
	for(var peopleNum = 0; peopleNum < people.length; peopleNum++){
	    for(var groupNum = 0; groupNum < people[peopleNum].length; groupNum++){
	    	try{  //  if/else로 타당성 검출이 아닌 try/catch로 검증함
	    		checkSameGroup(people[peopleNum][groupNum], peopleNum, groupNum);
	    	
		    }catch(e){
				console.log("fail: " + e.code + "," + e.message);
				console.log("people " + (peopleNum+1) + " group "+ (groupNum+1) );
			}
	    }
	}
	function checkSameGroup(arrGroup, peopleNum, groupNum) {
		for(var i =0 ; i < arrGroup.length; i++){
			for(var j = i; j < arrGroup.length; j++){ // 같은 수 중복 덧셈 허용(의뢰자 의도)
				
				var x = arrGroup[i];
				var y = arrGroup[j];
				
				resultTable[(x-1)][(y-1)]++;
				resultTable[(y-1)][(x-1)]++;
			}
		}	
	}
	
	/* 
	 * 파일에 출력하는 코드
	 */
	
	var output = fs.createWriteStream('./resultTable.txt');
	
	for(var i =0; i < resultTable.length; i++){
		for(var j =0; j < resultTable[i].length; j++){
			output.write(resultTable[i][j] + " ");
		}
		output.write("\n");	
	}
	output.end('End of File\n');
	output.on('finish', function(){
		console.log('write complete');
	}); 
});    